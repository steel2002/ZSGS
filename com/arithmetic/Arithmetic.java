package com.arithmetic;

public class Arithmetic {


	 public  int Add(int x1 ,int x2 ){      // Addition 
		return x1 + x2;

	 }



	public int Sub(int x1,int x2){           // Subtraction 
		return x1 -x2;
	}


	public int Mut(int x1,int x2){       // Multiplication 
		return x1 * x2;
	}


	public int Div(int x1,int x2){     // Division 
		return x1 /x2;
	}


	public int Mud (int x1,int x2){     // Modulo 
		return x1 % x2;

	}
}
    

